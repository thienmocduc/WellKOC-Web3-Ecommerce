# ── Ranking ─────────────────────────────────────────────────────────────────
@router.get("/ranking")
async def koc_ranking(current_user: User = Depends(require_role([UserRole.KOC])), db: AsyncSession = Depends(get_db)):
    """Return this KOC's current rank + pool position."""
    from app.models.pool_ranking import PoolRanking
    from datetime import date
    today = date.today()
    r = await db.execute(
        select(PoolRanking).where(PoolRanking.koc_id == current_user.id)
        .order_by(PoolRanking.year.desc(), PoolRanking.week.desc()).limit(1)
    )
    ranking = r.scalar_one_or_none()
    if not ranking:
        return {"rank": None, "pool": None, "score": 0, "commission_amount": 0}
    return {"rank": ranking.rank, "pool": ranking.pool, "score": float(ranking.score), "commission_amount": float(ranking.commission_amount), "week": ranking.week, "year": ranking.year}


