# ── Commissions ────────────────────────────────────────────────────────────
@router.get("/commissions")
async def koc_commissions(
    page: int = Query(1, ge=1),
    per_page: int = Query(20, ge=1, le=100),
    status: Optional[str] = Query(None),
    current_user: User = Depends(require_role([UserRole.KOC])),
    db: AsyncSession = Depends(get_db),
):
    """List commission records for this KOC."""
    from app.models.order import Commission, CommissionStatus
    q = select(Commission).where(Commission.koc_id == current_user.id)
    if status:
        q = q.where(Commission.status == status)
    total_r = await db.execute(select(func.count()).select_from(q.subquery()))
    total = total_r.scalar() or 0
    q = q.order_by(Commission.created_at.desc()).offset((page-1)*per_page).limit(per_page)
    r = await db.execute(q)
    items = r.scalars().all()
    return {
        "items": [{"id": str(c.id), "order_id": str(c.order_id), "amount": float(c.base_amount), "status": c.status, "tier": c.tier, "created_at": c.created_at.isoformat()} for c in items],
        "total": total, "page": page,
    }


